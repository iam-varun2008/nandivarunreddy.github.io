function syncScrollDirection() {
  const currentScrollY = window.scrollY || 0;
  if (Math.abs(currentScrollY - lastScrollY) > 2) {
    scrollDirection = currentScrollY < lastScrollY ? "up" : "down";
    document.body.dataset.scrollDirection = scrollDirection;
    lastScrollY = currentScrollY;
  }
  return currentScrollY;
}

function setLanguage(language) {
  currentLanguage = translations[language] ? language : "en";
  const dictionary = translations[currentLanguage];
  document.documentElement.lang = currentLanguage === "ko" ? "ko" : "en";
  document.title = dictionary.pageTitle;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) node.textContent = dictionary[key];
  });

  document.querySelectorAll("[data-i18n-label]").forEach((node) => {
    const key = node.dataset.i18nLabel;
    if (dictionary[key]) node.setAttribute("aria-label", dictionary[key]);
  });

  document.querySelectorAll("[data-i18n-title]").forEach((node) => {
    const key = node.dataset.i18nTitle;
    if (dictionary[key]) node.title = dictionary[key];
  });

  updateLightboxTriggerLabels();
  updateArtUploadLabels();

  document.querySelectorAll("[data-language-choice]").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.languageChoice === currentLanguage);
  });

  localStorage.setItem("portfolio-language", currentLanguage);
}

