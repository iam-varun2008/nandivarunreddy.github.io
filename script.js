function setupPortfolioCursor() {
  if (!window.matchMedia("(any-pointer: fine)").matches) return;

  const cursor = document.createElement("div");
  const ring = document.createElement("div");
  cursor.className = "portfolio-cursor";
  ring.className = "portfolio-cursor-ring";
  cursor.setAttribute("aria-hidden", "true");
  ring.setAttribute("aria-hidden", "true");
  document.body.append(cursor, ring);
  document.body.classList.add("has-custom-cursor");

  const moveCursor = (event) => {
    const x = `${event.clientX}px`;
    const y = `${event.clientY}px`;
    cursor.style.left = x;
    cursor.style.top = y;
    ring.style.left = x;
    ring.style.top = y;
  };

  const hoverSelector = [
    "a",
    "button",
    ".interactive-glow",
    ".media-slot",
    ".art-piece",
    ".is-lightbox-trigger",
    ".lightbox-close"
  ].join(",");

  window.addEventListener("pointermove", moveCursor, { passive: true });
  window.addEventListener("pointerdown", () => document.body.classList.add("cursor-pressed"));
  window.addEventListener("pointerup", () => document.body.classList.remove("cursor-pressed"));
  window.addEventListener("pointerleave", () => document.body.classList.add("cursor-hidden"));
  window.addEventListener("pointerenter", () => document.body.classList.remove("cursor-hidden"));

  document.addEventListener("pointerover", (event) => {
    if (event.target.closest(hoverSelector)) document.body.classList.add("cursor-hovering");
  });

  document.addEventListener("pointerout", (event) => {
    if (event.target.closest(hoverSelector)) document.body.classList.remove("cursor-hovering");
  });
}

function updateProjectMotion() {
  const viewportHeight = window.innerHeight || 1;
  document.querySelectorAll(".project-feature").forEach((card, index) => {
    const rect = card.getBoundingClientRect();
    const progress = Math.max(0, Math.min(1, (viewportHeight - rect.top) / (viewportHeight + rect.height)));
    const focus = Math.max(0, 1 - Math.abs(progress - 0.52) * 2.1);
    const direction = index % 2 === 0 ? 1 : -1;

    card.style.setProperty("--project-scale", (0.965 + focus * 0.035).toFixed(3));
    card.style.setProperty("--project-lift", `${((1 - focus) * 26).toFixed(1)}px`);
    card.style.setProperty("--project-tilt", `${((0.5 - progress) * 2.4 * direction).toFixed(2)}deg`);
    card.style.setProperty("--project-opacity", (0.78 + focus * 0.22).toFixed(3));
    card.style.setProperty("--media-slide", `${((0.5 - progress) * 64 * direction).toFixed(1)}px`);
    card.style.setProperty("--text-slide", `${((progress - 0.5) * 24 * direction).toFixed(1)}px`);
  });
}
function updateScrollEffects() {
  const currentScrollY = syncScrollDirection();
  const max = document.documentElement.scrollHeight - window.innerHeight;
  const progress = max > 0 ? currentScrollY / max : 0;
  scrollProgress.style.width = `${Math.min(progress * 100, 100)}%`;
  topbar.classList.toggle("is-scrolled", currentScrollY > 24);

  updateProjectMotion();

  document.querySelectorAll("[data-float]").forEach((node) => {
    const rect = node.getBoundingClientRect();
    const depth = Number(node.dataset.float) || 0;
    const midpoint = rect.top + rect.height / 2 - window.innerHeight / 2;
    const offset = Math.max(-44, Math.min(44, -midpoint * depth));
    node.style.setProperty("--float-y", `${offset}px`);
  });
}

