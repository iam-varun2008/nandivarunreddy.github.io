function setupImageLightbox() {
  const lightbox = document.querySelector("#imageLightbox");
  const image = document.querySelector("#lightboxImage");
  const caption = document.querySelector("#lightboxCaption");
  const closeButton = document.querySelector("#lightboxClose");
  let lastTrigger = null;

  if (!lightbox || !image || !caption || !closeButton) return;

  const closeLightbox = () => {
    lightbox.classList.remove("is-open");
    lightbox.setAttribute("aria-hidden", "true");
    document.body.classList.remove("lightbox-open");
    image.removeAttribute("src");
    caption.textContent = "";
    if (lastTrigger) lastTrigger.focus({ preventScroll: true });
  };

  const openLightbox = (slot) => {
    const src = slot.dataset.src;
    if (!src || src.toLowerCase().endsWith(".mp4")) return;
    const captionText = slot.closest(".media-figure")?.querySelector("figcaption")?.textContent?.trim() || slot.dataset.alt || "Project screenshot";
    lastTrigger = slot;
    image.src = src;
    image.alt = slot.dataset.alt || captionText;
    caption.textContent = captionText;
    lightbox.classList.add("is-open");
    lightbox.setAttribute("aria-hidden", "false");
    document.body.classList.add("lightbox-open");
    closeButton.focus({ preventScroll: true });
  };

  document.querySelectorAll(".project-gallery .media-slot[data-src]").forEach((slot) => {
    if (slot.dataset.src.toLowerCase().endsWith(".mp4")) return;
    slot.classList.add("is-lightbox-trigger");
    slot.tabIndex = 0;
    slot.setAttribute("role", "button");

    slot.addEventListener("click", () => openLightbox(slot));
    slot.addEventListener("keydown", (event) => {
      if (event.key !== "Enter" && event.key !== " ") return;
      event.preventDefault();
      openLightbox(slot);
    });
  });

  closeButton.addEventListener("click", closeLightbox);
  lightbox.addEventListener("click", (event) => {
    if (event.target === lightbox) closeLightbox();
  });
  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape" && lightbox.classList.contains("is-open")) closeLightbox();
  });

  updateLightboxTriggerLabels();
  updateArtUploadLabels();
}
