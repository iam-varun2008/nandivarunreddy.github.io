function setupArtUploads() {
  const dictionary = translations[currentLanguage] || translations.en;

  document.querySelectorAll(".art-piece").forEach((slot, index) => {
    if (slot.querySelector(".art-upload-control")) return;

    const input = document.createElement("input");
    input.type = "file";
    input.accept = "image/*";
    input.hidden = true;

    const label = document.createElement("label");
    label.className = "art-upload-control";
    label.setAttribute("role", "button");
    label.tabIndex = 0;

    const text = document.createElement("span");
    text.dataset.i18n = "addArtPhoto";
    text.textContent = dictionary.addArtPhoto || "Add Photo";

    label.append(text, input);

    label.addEventListener("keydown", (event) => {
      if (event.key !== "Enter" && event.key !== " ") return;
      event.preventDefault();
      input.click();
    });

    input.addEventListener("change", () => {
      const file = input.files && input.files[0];
      if (!file) return;
      if (slot.dataset.localObjectUrl) URL.revokeObjectURL(slot.dataset.localObjectUrl);

      const localUrl = URL.createObjectURL(file);
      slot.dataset.localObjectUrl = localUrl;
      const image = new Image();
      image.alt = `${slot.dataset.alt || "Uploaded artwork"} preview ${index + 1}`;
      image.onload = () => {
        Array.from(slot.children).forEach((child) => {
          if (child !== label) child.remove();
        });
        slot.prepend(image);
        slot.classList.add("has-local-art");
      };
      image.src = localUrl;
    });

    slot.append(label);
  });

  updateArtUploadLabels();
}
