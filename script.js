function replayStripeAnimation(node) {
  syncScrollDirection();
  if (!node.matches(".scene, .project-feature")) return;
  node.classList.toggle("wipe-reverse", scrollDirection === "up");
  node.classList.remove("stripe-replay");
  void node.offsetWidth;
  node.classList.add("stripe-replay");
}

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add("is-visible");
    });
  },
  { threshold: 0.14 }
);

document.querySelectorAll(".reveal, .scene, .project-feature").forEach((node) => revealObserver.observe(node));

const stripeObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting || entry.intersectionRatio < 0.52) return;
      replayStripeAnimation(entry.target);
    });
  },
  { threshold: 0.52 }
);

document.querySelectorAll(".scene, .project-feature").forEach((node) => stripeObserver.observe(node));

const sectionObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      const id = entry.target.id;
      navLinks.forEach((link) => link.classList.toggle("is-active", link.getAttribute("href") === `#${id}`));
    });
  },
  { rootMargin: "-38% 0px -52% 0px" }
);

sections.forEach((section) => sectionObserver.observe(section));

function fallbackTemplate(kind) {
  const terminalText = {
    "log-terminal": "$ python log_analyzer.py access.log\n[+] scanned 1,248 requests\n[!] repeated failed requests: 38\n[!] admin access attempts: 7\n[+] report saved: threat_report.html",
    "scan-terminal": "$ python port_scanner.py 192.168.1.10\n[+] authorized test target confirmed\n[+] 22/tcp ssh open\n[+] 80/tcp http open\n[+] risk summary: low to medium"
  };

  if (terminalText[kind]) {
    return `<div class="fallback-terminal"><span>Terminal output</span><code>${terminalText[kind]}</code></div>`;
  }

  if (kind === "log-report") {
    return `<div class="fallback-report"><div class="report-line"></div><div class="report-line short"></div><div class="report-metric"><strong>4</strong><span>Suspicious patterns</span></div><div class="report-line"></div><div class="report-line"></div></div>`;
  }

  if (kind === "scan-report") {
    return `<div class="fallback-report"><div class="report-line"></div><div class="report-line short"></div><div class="report-metric"><strong>3</strong><span>Open services</span></div><div class="report-line"></div><div class="report-line"></div></div>`;
  }

  if (kind === "video") {
    return `<div class="fallback-video"><div class="play-mark">▶</div></div>`;
  }

  return `<div class="fallback-portrait" aria-label="Graphite portrait placeholder"></div>`;
}

function replaceSlotContent(slot, content) {
  const persistentControls = Array.from(slot.querySelectorAll(".art-upload-control"));
  slot.replaceChildren();
  if (typeof content === "string") slot.insertAdjacentHTML("afterbegin", content);
  else slot.append(content);
  persistentControls.forEach((control) => slot.append(control));
}
function loadImageSlot(slot, src, alt, fallbackSrc) {
  const image = new Image();
  image.alt = alt || "Portfolio image";
  image.onload = () => replaceSlotContent(slot, image);
  image.onerror = () => {
    if (fallbackSrc && fallbackSrc !== src) loadImageSlot(slot, fallbackSrc, alt, null);
    else replaceSlotContent(slot, fallbackTemplate(slot.dataset.fallback || "portrait"));
  };
  image.src = src;
}

function loadVideoSlot(slot, src) {
  const video = document.createElement("video");
  video.muted = true;
  video.loop = true;
  video.playsInline = true;
  video.controls = true;
  video.preload = "metadata";
  video.onloadedmetadata = () => replaceSlotContent(slot, video);
  video.onerror = () => { replaceSlotContent(slot, fallbackTemplate(slot.dataset.fallback || "video")); };
  video.src = src;
}

function hydrateMediaSlots() {
  document.querySelectorAll(".media-slot[data-src]").forEach((slot) => {
    const src = slot.dataset.src;
    const fallbackSrc = slot.dataset.fallbackSrc;
    const alt = slot.dataset.alt;
    if (!src) return;
    if (src.toLowerCase().endsWith(".mp4")) loadVideoSlot(slot, src);
    else loadImageSlot(slot, src, alt, fallbackSrc);
  });
}


